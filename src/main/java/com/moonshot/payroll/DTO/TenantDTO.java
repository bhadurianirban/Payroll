package com.moonshot.payroll.DTO;

/**
 *
 * @author bhaduri
 */
public class TenantDTO {
    int id;
    String name;

    public TenantDTO(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    
}
